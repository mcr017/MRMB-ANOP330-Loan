# Remove poly_lasso_pipe and add these two:
@st.cache_resource
def load_artifacts():
    dtc_model = joblib.load('dtc_model.pkl')

    # Load the separated regression pieces
    poly_maker = joblib.load('poly_maker.pkl')
    lasso_model = joblib.load('lasso_model.pkl')

    scaler_class = joblib.load('scaler.pkl')
    expected_cols_class = joblib.load('expected_columns.pkl')
    scaler_poly = joblib.load('scaler_poly.pkl')
