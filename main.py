# --- 5. EXECUTE PREDICTION ---
st.divider()
if st.button("Run Risk Simulation", type="primary", use_container_width=True):

    # A. Package inputs into a DataFrame exactly as the model expects
    input_data = {
        'loan_amnt': loan_amnt,
        'term_num': term_num,
        'int_rate': int_rate,
        'grade': grade,
        'emp_length': emp_length,
        'home_ownership': home_ownership,
        'annual_inc': annual_inc,
        'verification_status': verification_status,
        'purpose': purpose,
        'dti': dti,
        'fico_range_high': fico_range_high,
        'open_acc': open_acc,
        'pub_rec': pub_rec,
        'delinq_2yrs': delinq_2yrs,
        'revol_bal': revol_bal,
        'revol_util': revol_util
    }

    raw_df = pd.DataFrame([input_data])

    # B. Feature Engineering
    raw_df['annual_inc_log'] = np.log1p(raw_df['annual_inc'])
    raw_df = raw_df.drop('annual_inc', axis=1)

    categorical_cols = raw_df.select_dtypes(include=['object']).columns.tolist()
    encoded_df = pd.get_dummies(raw_df, columns=categorical_cols)

    # C. Align columns for the Classifier
    # Reindex ensures the incoming dummy columns perfectly match the training columns. Missing columns get filled with 0.
    aligned_class_df = encoded_df.reindex(columns=expected_cols_class, fill_value=0)
    scaled_class_X = scaler_class.transform(aligned_class_df)

    # D. Align columns for the Regressor
    aligned_poly_df = encoded_df.reindex(columns=expected_cols_poly, fill_value=0)
    scaled_poly_X = scaler_poly.transform(aligned_poly_df)

    # E. Make Predictions
    prob_default = dtc_model.predict_proba(scaled_class_X)[0][1]

    poly_features = poly_maker.transform(scaled_poly_X)

    est_return = lasso_model.predict(poly_features)[0]

    # F. Business Logic
    if prob_default >= PROFIT_THRESHOLD:
        decision = "REJECT"
        decision_color = "red"
    else:
        decision = "APPROVE"
        decision_color = "green"

    # --- 6. DISPLAY RESULTS ---
    st.header("Simulation Results")

    res_col1, res_col2, res_col3 = st.columns(3)

    with res_col1:
        st.metric(label="Probability of Default", value=f"{prob_default * 100:.2f}%")

    with res_col2:
        st.metric(label="Estimated Return (PESS)", value=f"{est_return:.2f}%")

    with res_col3:
        st.markdown(f"### Decision: <span style='color:{decision_color}'>{decision}</span>", unsafe_allow_html=True)
        st.caption(f"Based on max risk threshold of {PROFIT_THRESHOLD * 100}%")
